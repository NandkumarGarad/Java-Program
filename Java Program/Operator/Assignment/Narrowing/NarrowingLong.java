class NarrowingLong 
{
	public static void main(String[] args) 
	{
		long l = 843;
		

		int i =(int) l;
		System.out.println(i);

		short s =(short) l;
		System.out.println(s);

		byte b =(byte) l;
		System.out.println(b);

		char ch =(char) l;
		System.out.println(ch);


		 
	}
}
